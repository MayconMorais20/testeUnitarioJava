package classes;

public class Calculadora {
    public Integer somar(Integer x, Integer y){
        return x + y;
    }
    public Integer subtrair(Integer x, Integer y){
        return x - y;
    }
    public Integer multiplicar(Integer x, Integer y){
        return x * y;
    }
    public Double dividir(Double x, Double y){
        return x / y;
    }
}
